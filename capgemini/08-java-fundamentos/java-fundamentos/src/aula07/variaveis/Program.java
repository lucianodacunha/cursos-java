package aula07.variaveis;

public class Program {

	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 30;
		int soma = num1 + num2;
		
		System.out.printf("Soma de %d + %d = %d\n", num1, num2, soma);
		
		String palavra1 = "Hello";
		String palavra2 = "Word";
		String mensagem = palavra1 + " " + palavra2;
		
		System.out.printf("Concatenação de %s + %s = %s\n", palavra1, palavra2, mensagem);

	}

}
